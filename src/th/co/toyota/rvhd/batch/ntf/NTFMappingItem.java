package th.co.toyota.rvhd.batch.ntf;

public class NTFMappingItem {
  private String traceTagCd;
  
  private String itemCode;
  
  private String itemGroup;
  
  private String lengthChk;
  
  private String partName;
  
  public String getTraceTagCd() {
    return this.traceTagCd;
  }
  
  public void setTraceTagCd(String traceTagCd) {
    this.traceTagCd = traceTagCd;
  }
  
  public String getItemCode() {
    return this.itemCode;
  }
  
  public void setItemCode(String itemCode) {
    this.itemCode = itemCode;
  }
  
  public String getItemGroup() {
    return this.itemGroup;
  }
  
  public void setItemGroup(String itemGroup) {
    this.itemGroup = itemGroup;
  }
  
  public String getLengthChk() {
    return this.lengthChk;
  }
  
  public void setLengthChk(String lengthChk) {
    this.lengthChk = lengthChk;
  }
  
  public String getPartName() {
    return this.partName;
  }
  
  public void setPartName(String partName) {
    this.partName = partName;
  }
}
